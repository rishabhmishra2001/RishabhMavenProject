package in.gov.isro;

public class Calculator {
	
	public int add(int x, int y){
		return x+y;
	}
	
	public int multiply(int x, int y){
		return x*y;
	}
	
	public int substract(int x, int y){
		return x-y;
	}
	
	public int divide(int x, int y){
		return x/y;
	}
	
	public int revert2(int x){
		return -x;
	}
}
